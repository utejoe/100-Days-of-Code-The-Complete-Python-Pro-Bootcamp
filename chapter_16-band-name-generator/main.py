# Step 1: Create a greeting for the program
print("Welcome to the band name generator.")
# Step 2: Ask the user for the city they grew up in
city = input("Which city did you grow up in?\n")
# Step 3: Ask the user for the name of their pet
pet = input("What is the name of your pet?\n")
# Step 4: Combine the city and pet names to generate the band name
print("Your band name could be: " + city + " " + pet)
# Step 5: Ensure the input cursor shows on a new line for readability
city += "\n"
pet += "\n"
